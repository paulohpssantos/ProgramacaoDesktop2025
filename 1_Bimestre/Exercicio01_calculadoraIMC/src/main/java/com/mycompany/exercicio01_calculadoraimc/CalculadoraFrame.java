    private void btCalcHomemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCalcHomemActionPerformed
        
        double imc = calculaIMC(tfPeso.getText(), tfAltura.getText());
        String msg = "";
        
        if(imc < 20.7){
            msg = "Atenção você está abaixo do peso ideal.";
        }else if(imc >= 20.7 && imc <= 26.4){
            msg = "Parabéns você está no peso ideal.";
        }else if(imc > 26.4 && imc <= 27.8){
            msg = "Você está ligeiramente acima do peso.";
        }else if(imc > 27.8 && imc <= 31.1){
            msg = "Atenção você está acima do peso.";
        }else if(imc > 31.1){
            msg = "Muita atenção você está obeso!";
        }    
        
        lbResultado.setText(msg);
    }//GEN-LAST:event_btCalcHomemActionPerformed


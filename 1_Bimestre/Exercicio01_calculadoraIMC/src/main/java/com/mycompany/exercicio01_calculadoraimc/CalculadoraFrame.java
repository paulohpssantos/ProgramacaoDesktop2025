    private void btCalcMulherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCalcMulherActionPerformed
        
        double imc = calculaIMC(tfPeso.getText(), tfAltura.getText());
        String msg = "";
        
        if(imc < 19.1){
            msg = "Atenção você está abaixo do peso ideal.";
        }else if(imc >= 19.1 && imc <= 25.8){
            msg = "Parabéns você está no peso ideal.";
        }else if(imc > 25.8 && imc <= 27.3){
            msg = "Você está ligeiramente acima do peso.";
        }else if(imc > 27.3 && imc <= 32.3){
            msg = "Atenção você está acima do peso.";
        }else if(imc > 32.3){
            msg = "Muita atenção você está obeso!";
        }    
        
        lbResultado.setText(msg);
    }//GEN-LAST:event_btCalcMulherActionPerformed

    private double calculaIMC(String p, String a){
        double peso = Double.parseDouble(p.replace(",", "."));
        double altura = Double.parseDouble(a.replace(",", "."));
        
        return peso/(altura * altura);
    }

